package com.lin.entity;

public class BlockDataSourceParamEntity extends AbstractEntity {
	private Integer block = 0;
	private Integer dataSourceParam = 0;
	private Integer param = 0;

	public Integer getBlock() {
		return block;
	}

	public void setBlock(Integer block) {
		this.block = block;
	}

	public Integer getDataSourceParam() {
		return dataSourceParam;
	}

	public void setDataSourceParam(Integer dataSourceParam) {
		this.dataSourceParam = dataSourceParam;
	}

	public Integer getParam() {
		return param;
	}

	public void setParam(Integer param) {
		this.param = param;
	}

}
